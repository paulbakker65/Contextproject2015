package net.tudelft.hi.e.common.enums;

/**
 * The possible compare operators.
 */
public enum CompareOperator {
  /**
   * equal.
   */
  EQ,
  /**
   * not equal.
   */
  NEQ,
  /**
   * greater than.
   */
  G,
  /**
   * greater than or equal.
   */
  GEQ,
  /**
   * less than.
   */
  L,
  /**
   * less than or equal.
   */
  LEQ,
  /**
   * not defined.
   */
  ND
}
