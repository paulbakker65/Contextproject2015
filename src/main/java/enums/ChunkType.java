package enums;

/**
 * Enum for the different Chunking Types.
 */
public enum ChunkType {
  /**
   * Chunking by Day.
   */
  DAY,
  /**
   * Chunking by Month.
   */
  MONTH,
  /**
   * Chunking by Year.
   */
  YEAR,
  /**
   * Chunking by Weekend.
   */
  WEEKEND,
  /**
   * Chunking by Patient.
   */
  PATIENT,
  /**
   * Chunking test value.
   */
  TEST
}
