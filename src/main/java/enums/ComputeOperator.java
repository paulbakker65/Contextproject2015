package enums;

public enum ComputeOperator {
  /**
   * Average of all values.
   */
  AVG,
  /**
   * Number of records.
   */
  COUNT,
  /**
   * Maximum of all values.
   */
  MAX,
  /**
   * Minimum of all values.
   */
  MIN,
  /**
   * Standard deviation of all values.
   */
  STDEV,
  /**
   * Sum of all values.
   */
  SUM
}
